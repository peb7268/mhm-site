// Utility function to generate page from template
function generateInteriorPage(templateName, customVariables = {}) {
    const config = templateConfigs[templateName];
    if (!config) {
        console.error(`Template "${templateName}" not found`);
        return null;
    }
    
    // Merge template config with custom variables
    const variables = { ...config, ...customVariables };
    
    // You would typically load the template HTML and replace variables here
    return variables;
}

